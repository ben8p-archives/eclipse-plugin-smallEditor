package smalleditor.editors.json.parsing;

/**
 * This class lists terminals used by the
 * grammar specified in "JSON.grammar".
 */
public class JsonGrammarTerminals {
	static public final short EOF = 0;
	static public final short STRING = 1;
	static public final short LCURLY = 2;
	static public final short LBRACKET = 3;
	static public final short NUMBER = 4;
	static public final short TRUE = 5;
	static public final short FALSE = 6;
	static public final short NULL = 7;
	static public final short RCURLY = 8;
	static public final short RBRACKET = 9;
	static public final short COMMA = 10;
	static public final short COLON = 11;
}
